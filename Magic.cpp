BOOL GlobalMagicTable::isInCryptApiList(string runningApiName)
{
    for (auto iter = _listCryptApi.begin(); iter != _listCryptApi.end(); iter++) {
        if (*iter == runningApiName) {
            return true;
        }
    }

    return false;
}

VOID GlobalMagicTable::_setupMapMagic(string cryptName, UINT64 *magicTable)
{
    for (size_t i = 0; i < sizeof(magicTable); i++) {
        _mapMagicNumberToCrypt[magicTable[i]] += cryptName;
    }
}

